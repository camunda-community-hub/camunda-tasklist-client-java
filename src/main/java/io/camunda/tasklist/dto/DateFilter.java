package io.camunda.tasklist.dto;

public class DateFilter {

  String from;
  String to;

  public DateFilter() {
  }

  public String getFrom() {
    return from;
  }

  public void setFrom(String from) {
    this.from = from;
  }

  public String getTo() {
    return to;
  }

  public void setTo(String to) {
    this.to = to;
  }
}
